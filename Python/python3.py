#Numerical Operation
Number1=int(input("Enter a number: "))
Number2= int(input("Enter another number: "))

print(Number1+Number2)
print(Number1-Number2)
print(Number1*Number2)
print(Number1/Number2)
print(Number1%Number2)
print(Number1**Number2)  #exponential
print(Number1//Number2)  #Floor
